// Função para buscar cidades que correspondem à busca
export const searchCities = (query: string, limit: number = 10): { city: string; state: string; display: string }[] => {
    if (!query || query.length < 2) return [];

    const normalizedQuery = query.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');

    return BRAZILIAN_CITIES
        .filter(item => {
            const normalizedCity = item.city.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
            return normalizedCity.includes(normalizedQuery);
        })
        .slice(0, limit)
        .map(item => ({
            city: item.city,
            state: item.state,
            display: `${item.city}, ${item.state}`
        }));
};
